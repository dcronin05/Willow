# Render the frames pixel by pixel
for frame_idx, frame_data in enumerate(frames):
    x_offset = frame_idx * 16
    for y in range(32):
        row = frame_data[y]
        for x in range(16):
            char = row[x]
            px = x_offset + x
            py = y
            
            if char == '#':
                draw.point((px, py), fill=black)
            elif char == 'O':
                draw.point((px, py), fill=white)
            elif char == '%':
                # Checkerboard dither pattern based on screen coordinates
                if (px + py) % 2 == 0:
                    draw.point((px, py), fill=black)
                else:
                    draw.point((px, py), fill=white)

